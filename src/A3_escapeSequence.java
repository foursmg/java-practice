public class A3_escapeSequence {
    public static void main(String[]args){
        System.out.println("inserting quote in print message using backslash");
        System.out.println("he said \"Debit the Receiver,Credit the giver\"");
        System.out.println("\\n newline");
        System.out.println("hey\nstop");
        System.out.println("\\b backspace");
        System.out.println("hey\bstop");
        System.out.println("\\t tab");
        System.out.println("hey\tstop");

    }
}
